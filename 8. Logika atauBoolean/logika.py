# seperti logika matematika
# and (&)
# or (|)
# not (not)
# xor

print("AND:")
print("True & True  =", True & True)
print("True & False =", True & False)
print("False & True =", False & True)
print("False & False =", False & False)
# and itu akan true ketika semuanya true

print("OR:")
print("True | True  =", True | True)
print("True | False =", True | False)
print("False | True =", False | True)
print("False | False =", False | False)
# or itu akan false ketika semuanya false

print("NOT:")
a = True
print("a =", a)
print("not a =", not a)
# not itu kebalikan, jika true => false, false => true

print("XOR:")
print(True ^ False)
# xor itu akan true ketika satu true dan lainnya false