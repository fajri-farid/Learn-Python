# casting = mengubah satu tipe data ke tipe data lain
# tipe data = int, float, str, bool

# Konversi ke int
data_bool = True 
data_int = int(data_bool)
print("Isian data: ", data_int)  
print("tipe data : ", type(data_int)) 
# True menjadi 1
# False menjadi 0


# Konversi ke float
data_bool = True 
data_float = float(data_bool)
print("Isian data: ", data_float)  
print("tipe data : ", type(data_float)) 
# True menjadi 1.0
# False menjadi 0.0


# Konversi ke string
data_bool = True 
data_str = str(data_bool)
print("Isian data: ", data_str)  
print("tipe data : ", type(data_str)) 
# True menjadi "True"
# False menjadi "False"
